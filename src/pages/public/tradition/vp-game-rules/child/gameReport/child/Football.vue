            </ul>
          </li>
          <li>如果赛事并未进行点球，所有注单将被取消。</li>
          <li>在90分钟完场时间内以及加时赛踢进的点球将不计算在内。</li>
          <li>如果赛事在点球赛时中断，而在赛事中断前已有明确结果并且之后没有任何显著会影响投注结果的情况，大/小盘注单才会被结算。若遇到任何其他情况，注单将一律被取消。
          </li>
        </ul>
