          2.34.4. 第一/最后一个角球</p>
        <ul style="list-style: decimal;">
          <li>预测在90分钟完场时间内，第一或最后获得角球的球队。</li>
          <li>如果赛事在获得第一个角球后中断，所有第一个角球的注单将保持有效。</li>
